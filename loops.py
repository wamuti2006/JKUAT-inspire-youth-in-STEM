#loops are and essential part of programming.They allow you to repeat a blockof code multiple times
#there are two types of loops in python,"for"and "while".

#"for"loop
#The "for" loop is used to iterate over over a sequence (list,tuple,string) or other iterable objects
#let us literate over a list
fruits=("apple","banana","cherry","pineapple")

for fruit in fruits:
    print(fruit)

for i in range(10):
    print(i)

for i in range(7):
    print(i)

for char in "python":
    print(char)            